# Control Structures in Python
# If-Else
x = 10
if x > 5:
    print("x is greater than 5")
else:
    print("x is less than or equal to 5")

# For Loop
for i in ['mosh', 'john', 'sarah']:
    print(i)


# While Loop
i = 1
while i <= 5:
    print(i)
    i = i + 1
print("done")

#Nested loop
for x in range(4):
    for y in range(3):
        print(f'({x}, {y})')





